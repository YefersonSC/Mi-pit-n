#Permite ejecutar en repetidas ocasiones las instrucciones o líneas de código indicadas por el programador
#Con lo cual no existe la necesidad de duplicar líneas de código para ejecutarlas en más de una ocasión 
#En python se cuenta con el ciclo "while", permite repetir la ejecución de un grupo de instrucciones
#Mientras la condición del ciclo o bucle se cumpla, las instrucciones se seguirán ejecutando

#Ejemplo 1
x = 1
while x < 3:
    print(x)
    x += 1
print("Fin.")

#Ejemplo 2
x = 1
while x < 9:
    print("Yeferson")
    x += 1